import fs from "node:fs";
import path from "node:path";
import vm from "node:vm";

const root = path.resolve(import.meta.dirname, "..");
const loadScript = (relativePath, globalName) => {
  const context = { window: {} };
  vm.runInNewContext(fs.readFileSync(path.join(root, relativePath), "utf8"), context);
  return context.window[globalName];
};
const rain = loadScript("story-data.js", "MIST_DATA");
const dorm = loadScript("assets/stories/dormitory-rollcall/story-data.js", "MIST_DORMITORY_DATA");
const rainManifest = JSON.parse(fs.readFileSync(path.join(root, "assets/stories/rain-call/audio/voice-manifest.json"), "utf8"));
const dormManifest = JSON.parse(fs.readFileSync(path.join(root, "assets/stories/dormitory-rollcall/audio/voice-manifest.json"), "utf8"));
const broadcast = loadScript("assets/stories/dormitory-rollcall/broadcast-audio-contract.js", "DORMITORY_BROADCAST_AUDIO_CONTRACT");

const termList = ["周婉宁", "许棠", "四一七", "零点十七分", "零点四十四分", "一点十三分", "二零一四年", "三百一十九", "三百二十"];
const normalize = (text) => String(text || "")
  .replace(/00:17/g, "零点十七分")
  .replace(/00:44/g, "零点四十四分")
  .replace(/01:13/g, "一点十三分")
  .replace(/2014/g, "二零一四年")
  .replace(/417/g, "四一七")
  .replace(/319/g, "三百一十九")
  .replace(/320/g, "三百二十");
const describeEntries = (story, manifest) => Object.values(manifest.entries || {}).map((entry) => {
  const source = entry.kind === "ending" ? story.endings?.[entry.endingId] : story.nodes?.[entry.nodeId];
  return { id: entry.id, kind: entry.kind, roleId: entry.roleId, speaker: entry.speaker, text: normalize(source?.text || "") };
});
const allEntries = [
  ...describeEntries(rain, rainManifest),
  ...describeEntries(dorm, dormManifest),
  ...(broadcast.cues || []).map((cue) => ({ id: `broadcast__${cue.audioId}`, kind: "broadcast-cue", roleId: "dorm_broadcast", speaker: "Broadcast", text: normalize(cue.line) })),
];
const linesFor = (term) => allEntries.filter((entry) => entry.text.includes(term)).map((entry) => `\`${entry.id}\` (${entry.speaker})`).join(", ") || "No generated entry found";
const rainNames = [...new Set(Object.values(rain.nodes || {}).map((node) => node.speaker).filter((name) => name && name !== "Narrator"))].sort();
const telephoneEntries = allEntries.filter((entry) => /电话|通话|录音|语音/.test(entry.text));
const endingEntries = allEntries.filter((entry) => entry.kind === "ending");
const report = `# Voice Pronunciation Review

Generated by \`node scripts/generate-voice-pronunciation-review.mjs\`. This is a targeted **manual listening queue**, not an automatic pronunciation pass.

## Required terms

| Term | Generated entry IDs requiring human listening |
| --- | --- |
${termList.map((term) => `| ${term} | ${linesFor(term)} |`).join("\n")}

## First-story names

Listen to every instance for these rain-call speakers: ${rainNames.join("、")}。

## Context-sensitive queues

- Telephone and recording lines: ${telephoneEntries.map((entry) => `\`${entry.id}\``).join(", ") || "None"}
- Every ending's final line: ${endingEntries.map((entry) => `\`${entry.id}\``).join(", ")}
- All 14 institutional broadcast cues: ${(broadcast.cues || []).map((cue) => `\`broadcast__${cue.audioId}\``).join(", ")}

## Reviewer instruction

Use the local \`tools/voice-review/\` page. Mark only local review status there; do not treat a browser mark as production sign-off. Escalate any surname, number, time expression, recording effect, or final sentence that is unclear, incorrectly stressed, or too fast for a phone speaker.
`;

const output = path.join(root, "docs/VOICE_PRONUNCIATION_REVIEW.md");
fs.writeFileSync(output, report, "utf8");
console.log(`Wrote ${path.relative(root, output)}. targetTerms=${termList.length}; telephoneOrRecording=${telephoneEntries.length}; endings=${endingEntries.length}`);
